import { Component, inject } from '@angular/core';
import { AuthService } from '../../services/auth';
import { Router} from '@angular/router';

@Component({
  selector: 'app-auth',
  imports: [],
  templateUrl: './auth.html',
  styleUrl: './auth.css',
})
export class Auth {
  titulo='Hola'
  autenticando=false
  mensajeError=""
//Inyeccion de dependencias
  private authService  = inject(AuthService)
  private router = inject(Router)

  //Funcion  que revisa la  autenticacion -asincrona
 async IniciarSesionConGoogle(): Promise<void>{
  this.autenticando =true
  this.mensajeError=""

    try {
      console.log('Ilamo a la funcion')
      const usuario = await this.authService.iniciarSesion()
      //Vamos a simular un usuario ya creado
      /*let usuario=null
      usuario = await new Promise ((resolve) =>{
        setTimeout(()=>resolve({nombre:'usuario de prueba'}),1000)
      })*/
      if(usuario){
        await this.router.navigate(['/chat'])
      }else{
        this.mensajeError = "Error al autenticar"
        console.error("Error al autenticar en try")
      }

    } catch (error: any){
    //validacion de algunos posibles errores
    if(error.code == "auth/popup-closed-by-user"){
      console.error('Error:Cerraste la ventana emergente')
    }else if(error.code == 'auth/popup-blocked'){
      console.error('El navegador bloqueo la ventana emergente')
    }else if (error.code == 'auth/network-request-failed'){
      console.error('Problemas con la conexiÃ³n a internet')
    }
    }finally{
      this.autenticando=false
    }
  }
//Verificar que si el usuario ya esta autenticado y redirigirlo al chat de una vez
//el ngOnInit sirve para ejecutar codigo al iniciar el componente, en este caso para verificar si el usuario ya esta autenticado y redirigirlo al chat de una vez


 ngOnInit(){

  this.authService.estaAutenticado$.subscribe(autenticado =>{
    if(autenticado){
  this.router.navigate(['/chat'])
 }
  });
  }

}